package br.com.segundoprojeto;

public class LeituraDeArquivos {
    Integer index;
    Integer year;
    Integer age;
    String name;
    String movie;

    public LeituraDeArquivos(Integer index, Integer year, Integer age, String name, String movie){
        this.index = index;
        this.year = year;
        this.age = age;
        this.name = name;
        this.movie = movie;
    }

}
